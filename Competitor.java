package AP_CSA_2025_FRQ.QUESTION_3;

public class Competitor {
    private String name;
    private int rank;

    public Competitor(String n, int initialRank) {
        name = n;
        rank = initialRank;
    }

    public String getName() {
        return name;
    }

    public int getRank() {
        return rank;
    }


}
